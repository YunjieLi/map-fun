import { existsSync, readFileSync, writeFileSync } from 'fs'
import { dirname, resolve } from 'path'
import { fileURLToPath } from 'url'

const __dirname = dirname(fileURLToPath(import.meta.url))
const root = resolve(__dirname, '..')

function readTokenFromEnvFile() {
  const envPath = resolve(root, '.env')
  if (!existsSync(envPath)) return ''

  return readFileSync(envPath, 'utf8')
    .split('\n')
    .map((line) => line.trim())
    .filter((line) => line && !line.startsWith('#'))
    .reduce((token, line) => {
      const match = line.match(/^(?:VITE_MAPBOX_TOKEN|MAPBOX_ACCESS_TOKEN)=(.*)$/)
      if (!match) return token
      return match[1].trim().replace(/^['"]|['"]$/g, '')
    }, '')
}

const token =
  process.env.VITE_MAPBOX_TOKEN ||
  process.env.MAPBOX_ACCESS_TOKEN ||
  readTokenFromEnvFile()

const targets = [
  'src/swordsmen/mapbox-config.js',
  'src/swordsmen/archive/mapbox-config.js',
  'src/spanish-missions/mapbox-config.js',
]

const content = [
  '// Generated by scripts/generate-mapbox-config.mjs — do not commit',
  `window.MAPBOX_ACCESS_TOKEN = ${JSON.stringify(token)};`,
  '',
].join('\n')

for (const target of targets) {
  writeFileSync(resolve(root, target), content)
}

if (!token) {
  console.warn(
    'Warning: VITE_MAPBOX_TOKEN is not set. Copy .env.example to .env or add the secret in GitHub Actions.',
  )
} else {
  console.log(`Wrote Mapbox config for ${targets.length} map(s).`)
}
